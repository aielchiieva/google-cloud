package co.cask.gcp.datastore.sink;

import co.cask.cdap.api.data.batch.OutputFormatProvider;
import co.cask.gcp.datastore.sink.util.DatastoreSinkConstants;
import com.google.common.collect.ImmutableMap;

import java.util.Map;
import java.util.Objects;

/**
 * Provides DatastoreOutputFormat's class name and configuration.
 */
public class DatastoreOutputFormatProvider implements OutputFormatProvider {

  private final Map<String, String> configMap;

  /**
   * Gets properties from {@link DatastoreSink} and stores them as properties in map for {@link DatastoreRecordWriter}.
   *
   * @param config datastore configuration
   */
  public DatastoreOutputFormatProvider(DatastoreSinkConfig config) {
    ImmutableMap.Builder<String, String> builder = new ImmutableMap.Builder<String, String>()
      .put(DatastoreSinkConstants.CONFIG_PROJECT, config.getProject())
      .put(DatastoreSinkConstants.CONFIG_USE_AUTOGENERATED_KEY, Boolean.toString(config.shouldUseAutoGeneratedKey()))
      .put(DatastoreSinkConstants.CONFIG_BATCH_SIZE, Integer.toString(config.getBatchSize()));

    if (Objects.nonNull(config.getServiceAccountFilePath())) {
      builder.put(DatastoreSinkConstants.CONFIG_SERVICE_ACCOUNT_FILE_PATH, config.getServiceAccountFilePath());
    }
    this.configMap = builder.build();
  }

  @Override
  public String getOutputFormatClassName() {
    return DatastoreOutputFormat.class.getName();
  }

  @Override
  public Map<String, String> getOutputFormatConfiguration() {
    return configMap;
  }
}
